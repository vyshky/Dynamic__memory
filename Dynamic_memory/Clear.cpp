#include"Header.h"

//Удаление двумерного динамического массива:
void Clear(int** arr, int m)
{
	//1)Удаление строки:
	for (int i = 0; i < m; i++)
	{
		delete[] arr[i];
	}
	//2)Удаляем массив указателей
	delete[] arr;
	cout << "===============================================" << endl << "Массив arr удален" << endl
		<<	"==============================================="<< endl;
}